<h1>By skill, we mean e.g. SPY _SKILL_ ↩️</h1>

<p>What are all the skills that Fat Neo has? How do they work? We try to answer that below briefly, 
    with links to more detailed sources and methodology in the blog section. </p>

<ul>
  <li><strong>   :</strong> Entering only _TICKER_ (e.g. SPY ↩️) is the default command, gets you a basic Options Implied price outlook for the week ahead</li>
  <li><strong>DOOM:</strong> Entering _TICKER_ DOOM is gets you the Options Implied probability of a strong drop in the month ahead</li>
  <li><strong>KELLY:</strong> Entering _TICKER_ KELLY is an experimental feature, that does optimal Kelly bet sizing. Perhaps one of the most overlooked tool by investors.</li>
  <li><strong>CALL:</strong> Entering _TICKER_ CALL is an experimental feature, helps sort and find the optimal covered call to sell in the month ahead.</li>
  <li><strong>VOLUME</strong> Entering _TICKER_ VOLUME gets you a basic comparative view of stock volume vs average levels experienced recently</li>
  <li><strong>WSB:</strong> Entering _TICKER_ WSB is an experimental feature, gets you a relative feel for volume of mentions on Reddit's WallStreetBets</li>
    <li><strong>_YOUR SUGGESTION HERE_:</strong> We are just getting started. Most of the skills above came from users like you. Connect below.</li>
</ul>

