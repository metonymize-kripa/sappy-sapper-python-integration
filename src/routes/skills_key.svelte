<h1>Skills, the Key</h1>

<p>By a Fat Neo skill we mean something like "SPY SKILL ↩️". It is how Fat Neo helps you trade faster, smarter, funner.</p>
<p>What are all the skills that Fat Neo has? How do they work? We try to answer these questions briefly on this page,
    with links to more detailed sources and methodology that you can find in the blog section. </p>

<ul>
  <li><strong> BLANK:</strong> Entering only <a href="https://fatneo.vercel.app/blog/tutorial-range">TICKER</a> (e.g. SPY ↩️) is the default command. It gets you a basic Options Implied price outlook for the week ahead</li>
  <li><strong>💥 DOOM:</strong> Entering <a href="https://fatneo.vercel.app/blog/tutorial-doom">TICKER DOOM</a> gets you the Options Implied probability of a strong drop in the month ahead</li>
  <li><strong>🎲 KELLY:</strong> Entering <a href="https://fatneo.vercel.app/blog/tutorial-kelly">TICKER KELLY</a> is an experimental feature, to do Options Implied Kelly optimal bet sizing. Perhaps one of the most overlooked tool by investors.</li>
  <li><strong>📈 CALL:</strong> Entering <a href="https://fatneo.vercel.app/blog/tutorial-call">TICKER CALL</a> is an experimental feature, to find the optimal covered call/call-spread to sell in the month ahead</li>
  <li><strong>🔊 VOLUME:</strong> Entering <a href="https://fatneo.vercel.app/blog/tutorial-volume">TICKER VOLUME</a> gets you a basic comparative view of stock volume vs the recent average</li>
  <li><strong>💵 DIV:</strong> Entering <a href="https://fatneo.vercel.app/blog/tutorial-div">TICKER DIV</a> gets you a basic view of stock dividends</li>
  <li><strong>🎢 WSB:</strong> Entering TICKER WSB is an experimental feature, offering a directional feel for sentiment on Reddit's WallStreetBets (e.g. Mentions)</li>
  <li><strong>🦉 WISE:</strong> Entering TICKER WISE is an experimental feature, offering a directional feel of a curated group of experts who Tweet </li>
    <li><strong>📯 USER_X:</strong> We are just getting started and are all ears. Most of the skills above came from users like you. Connect below.</li>
</ul>

